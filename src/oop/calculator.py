class Calc:
    
    def add(num1: float, num2: float) -> float :
        return num1 + num2
    
    def minus(num1: float, num2: float) -> float:
        return num1 - num2
    
    def mul(num1: float, num2: float) -> float:
        return num1 * num2
    
    def div(num1 : float, num2: float) -> float:
        return num1 / num2